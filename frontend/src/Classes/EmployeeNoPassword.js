export class EmployeeNoPassword
{
    constructor(id, firstName, lastName, email, company_id, team_id, is_owner, phone, dateOfBirth, role)
    {
        this.id = id;
        this.firstName = firstName;
        this.lastName = lastName;
        this.email = email;
        this.company_id = company_id;
        this.team_id = team_id;
        this.is_owner = is_owner;
        this.phone = phone;
        this.dateOfBirth = dateOfBirth;
        this.role = role;
    }
}